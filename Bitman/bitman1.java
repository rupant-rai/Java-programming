package Bitman;

public class bitman1 {
    public static void main( String[] args){
        System.out.println(5& 6);
        System.out.println(5 | 6);
        System.out.println(5^6);
        System.out.println(~5);//=-6
        System.out.println(~0);//=-1
        System.out.println(5<<2);//a<<b=a*2pow b
        System.out.println(6>>1);
    }
}
